package desafios;

// Média de Duas Notas
public class SegundoDesafio {
    public static void main(String[] args) {
        double primeiraNota = 8.5;
        double segundaNota = 10;
        double average = (primeiraNota + segundaNota) / 2;

        System.out.println(String.format("A média das duas notas é: %.2f", average));
    }
}
